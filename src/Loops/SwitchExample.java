package Loops;

public class SwitchExample {

	public static void main(String[] args) {
		
		int n=70;
		switch(n) {
		case 30:System.out.println("gear");
		break;
		case 40:System.out.println("hardware");
		break;
		case 50:System.out.println("software");
		break;
		case 60:System.out.println("computer");
		break;
		case 70:System.out.println("thilak");
		break;
		case 80:System.out.println("friend");
		}

	}

}
